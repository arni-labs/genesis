fn route_variant_generator(
    ctx: &Context,
    base_url: &str,
    headers: &[(String, String)],
    work_item_id: &str,
    generation_id: &str,
    work_item_fields: &Value,
    output: &Value,
) -> Result<Value, String> {
    let generation = get_entity(ctx, base_url, headers, "Generations", generation_id)?;
    let generation_fields = state_fields(&generation);
    let episode_id = field_str(&generation_fields, &["EpisodeId"]);
    let parent_version_id = field_str(&generation_fields, &["ParentVersionId"]);
    let episode = get_entity(ctx, base_url, headers, "Episodes", &episode_id)?;
    let episode_fields = state_fields(&episode);
    let worker_run_id = field_str(work_item_fields, &["WorkerRunId"]);
    let summary = nonempty(
        lookup_string_deep(output, &["summary", "Summary", "reasoning_summary"]),
        format!("Variant generated by WorkItem {work_item_id}"),
    );
    let app_ref = lookup_string_deep(output, &["app_ref", "appRef", "AppRef"]);
    let branch_ref = nonempty(
        lookup_string_deep(output, &["branch_ref", "branchRef", "BranchRef"]),
        format!(
            "directed-evolution/{generation_id}/{}",
            short_id(work_item_id)
        ),
    );
    let runtime_ref = nonempty(
        lookup_string_deep(output, &["runtime_ref", "runtimeRef", "RuntimeRef"]),
        app_ref.clone(),
    );
    let changed_files_json =
        lookup_value_deep(output, &["changed_files", "changedFiles", "ChangedFiles"])
            .unwrap_or_else(|| json!([]))
            .to_string();
    let diff_ref = lookup_string_deep(output, &["diff_ref", "diffRef", "DiffRef"]);
    let diff_patch = lookup_string_deep(output, &["diff_patch", "diffPatch", "DiffPatch", "patch"]);

    let variant_id = create_entity(ctx, base_url, headers, "Variants")?;
    let mutation_id = create_entity(ctx, base_url, headers, "Mutations")?;
    post_directed_action(
        ctx,
        base_url,
        headers,
        "Mutations",
        &mutation_id,
        "RecordMutationWithPatch",
        json!({
            "VariantId": variant_id,
            "Summary": summary,
            "ChangedFilesJson": changed_files_json,
            "DiffRef": diff_ref,
            "DiffPatch": diff_patch,
            "WorkerRunId": worker_run_id,
        }),
    )?;
    post_directed_action(
        ctx,
        base_url,
        headers,
        "Variants",
        &variant_id,
        "RecordVariantMutation",
        json!({
            "EpisodeId": episode_id,
            "GenerationId": generation_id,
            "MutationId": mutation_id,
            "AppRef": app_ref,
            "BranchRef": branch_ref,
            "Summary": summary,
            "ChangedFilesJson": changed_files_json,
            "DiffPatch": diff_patch,
            "WorkerRunId": worker_run_id,
        }),
    )?;
    post_directed_action(
        ctx,
        base_url,
        headers,
        "Generations",
        generation_id,
        "RecordGeneratedVariant",
        json!({
            "VariantId": variant_id,
            "WorkerRunId": worker_run_id,
        }),
    )?;
    post_directed_action(
        ctx,
        base_url,
        headers,
        "Variants",
        &variant_id,
        "MarkVariantActive",
        json!({
            "RuntimeRef": runtime_ref,
            "EvidenceArtifactId": "",
        }),
    )?;

    let stage_ids =
        parse_json_string_array(&field_str(&episode_fields, &["EvaluationStageIdsJson"]));
    let mut queued_stage_result_ids = Vec::new();
    for stage_id in stage_ids {
        let stage = get_entity(ctx, base_url, headers, "EvaluationStages", &stage_id)?;
        let stage_fields = state_fields(&stage);
        let stage_kind = field_str(&stage_fields, &["StageKind"]);
        let stage_name = field_str(&stage_fields, &["StageName"]);
        let stage_result_id = create_entity(ctx, base_url, headers, "StageResults")?;
        post_directed_action(
            ctx,
            base_url,
            headers,
            "StageResults",
            &stage_result_id,
            "PlanStageResult",
            json!({
                "EpisodeId": episode_id,
                "GenerationId": generation_id,
                "VariantId": variant_id,
                "EvaluationStageId": stage_id,
            }),
        )?;
        if stage_kind.to_ascii_lowercase().contains("simulated") {
            queue_simulated_user_trials(
                ctx,
                base_url,
                headers,
                &episode_fields,
                &stage_fields,
                &episode_id,
                generation_id,
                &variant_id,
                &stage_id,
                &stage_result_id,
                &stage_name,
                &runtime_ref,
                &app_ref,
                &summary,
            )?;
        } else if defer_stage_until_simulated_users(&stage_fields) {
            // Post-trial stages, especially Datadog telemetry, must wait until
            // simulated users have actually exercised the variant runtime.
        } else {
            let role = evaluator_role_for_stage(&stage_fields);
            let evaluation_work_item_id = queue_stage_evaluation_work_item(
                ctx,
                base_url,
                headers,
                &episode_fields,
                &stage,
                &variant_id,
                generation_id,
                &episode_id,
                &stage_id,
                &stage_result_id,
                &summary,
                &app_ref,
                &runtime_ref,
                &role,
                &parent_version_id,
            )?;
            post_directed_action(
                ctx,
                base_url,
                headers,
                "StageResults",
                &stage_result_id,
                "StartStageResult",
                json!({
                    "EpisodeId": episode_id,
                    "GenerationId": generation_id,
                    "VariantId": variant_id,
                    "EvaluationStageId": stage_id,
                    "WorkItemId": evaluation_work_item_id,
                }),
            )?;
        }
        queued_stage_result_ids.push(stage_result_id);
    }

    Ok(json!({
        "routed": "variant_generator",
        "variant_id": variant_id,
        "mutation_id": mutation_id,
        "queued_stage_result_ids": queued_stage_result_ids,
    }))
}

fn defer_stage_until_simulated_users(stage_fields: &Value) -> bool {
    stage_requires_datadog(stage_fields) || evaluator_role_for_stage(stage_fields) == "telemetry_evaluator"
}

fn evaluator_role_for_stage(stage_fields: &Value) -> String {
    let executor = field_str(stage_fields, &["ExecutorKind"]);
    if stage_evaluator_role(&executor) {
        return executor;
    }
    let kind = field_str(stage_fields, &["StageKind"]).to_ascii_lowercase();
    if kind.contains("telemetry") || kind.contains("datadog") {
        "telemetry_evaluator".to_string()
    } else if kind.contains("state") || kind.contains("spec") {
        "state_verifier".to_string()
    } else if kind.contains("wasm") {
        "wasm_evaluator".to_string()
    } else {
        "reviewer".to_string()
    }
}

fn queue_stage_evaluation_work_item(
    ctx: &Context,
    base_url: &str,
    headers: &[(String, String)],
    episode_fields: &Value,
    stage: &Value,
    variant_id: &str,
    generation_id: &str,
    episode_id: &str,
    stage_id: &str,
    stage_result_id: &str,
    summary: &str,
    app_ref: &str,
    runtime_ref: &str,
    role: &str,
    parent_version_id: &str,
) -> Result<String, String> {
    let work_item_id = create_entity(ctx, base_url, headers, "WorkItems")?;
    post_paw_orchestration_action(
        ctx,
        base_url,
        headers,
        "WorkItems",
        &work_item_id,
        "QueueWorkItem",
        json!({
            "Role": role,
            "TargetEntityType": "StageResult",
            "TargetEntityId": stage_result_id,
            "PromptRef": format!("literal:{}", evaluation_prompt(ctx, base_url, headers, episode_fields, stage, variant_id, generation_id, episode_id, stage_id, stage_result_id, &work_item_id, summary, app_ref, runtime_ref)?),
            "ContextRef": format!("variant:{variant_id}"),
            "OutputSchemaRef": "directed-evolution.stage-evaluation.v1",
            "RequiredCapabilities": if role == "telemetry_evaluator" { "local_codex,datadog_query" } else { "local_codex,review" },
            "Lane": if role == "telemetry_evaluator" { "telemetry-evaluation" } else { "evaluation" },
            "ExclusiveKey": format!("stage-result:{stage_result_id}:{role}"),
            "CorrelationJson": json!({
                "episode_id": episode_id,
                "direction_id": field_str(episode_fields, &["DirectionId"]),
                "generation_id": generation_id,
                "variant_id": variant_id,
                "evaluation_stage_id": stage_id,
                "stage_result_id": stage_result_id,
                "parent_version_id": parent_version_id,
                "runtime_ref": runtime_ref,
                "role": role,
            }).to_string(),
        }),
    )?;
    Ok(work_item_id)
}

/// The organism's configured external runtime target (ADR-0023):
/// where simulated users exercise the app, which tenant, which env
/// var NAMES resolve the bearer token, and which Datadog service owns
/// the runtime's telemetry.
struct OrganismRuntimeTarget {
    base_url: String,
    tenant: String,
    auth_env_vars: String,
    datadog_service: String,
}

fn organism_runtime_target(
    ctx: &Context,
    base_url: &str,
    headers: &[(String, String)],
    organism_id: &str,
) -> Result<OrganismRuntimeTarget, String> {
    if organism_id.trim().is_empty() {
        return Err("episode has no OrganismId; cannot resolve a runtime target".to_string());
    }
    let organism = get_entity(ctx, base_url, headers, "Organisms", organism_id)?;
    let fields = state_fields(&organism);
    let runtime_base_url = field_str(&fields, &["RuntimeBaseUrl"]);
    let tenant = field_str(&fields, &["RuntimeTenantId"]);
    if runtime_base_url.trim().is_empty() || tenant.trim().is_empty() {
        return Err(format!(
            "organism {organism_id} has no configured runtime target; dispatch Organism.ConfigureOrganismRuntime (ADR-0023) before trials can run"
        ));
    }
    Ok(OrganismRuntimeTarget {
        base_url: runtime_base_url,
        tenant,
        auth_env_vars: field_str(&fields, &["RuntimeAuthEnvVarsJson"]),
        datadog_service: field_str(&fields, &["DatadogService"]),
    })
}

fn queue_simulated_user_trials(
    ctx: &Context,
    base_url: &str,
    headers: &[(String, String)],
    episode_fields: &Value,
    stage_fields: &Value,
    episode_id: &str,
    generation_id: &str,
    variant_id: &str,
    stage_id: &str,
    stage_result_id: &str,
    stage_name: &str,
    runtime_ref: &str,
    app_ref: &str,
    variant_summary: &str,
) -> Result<Vec<String>, String> {
    // ADR-0018: trials enumerate from a FROZEN plan only. No plan or an
    // unfrozen plan refuses loudly instead of silently running a 1x1
    // synthetic census.
    let plan_id = field_str(episode_fields, &["SimulatedUserPlanId"]);
    if plan_id.trim().is_empty() {
        return Err(format!(
            "episode {episode_id} has no SimulatedUserPlanId; trials require a frozen simulated-user plan"
        ));
    }
    let plan = get_entity(ctx, base_url, headers, "SimulatedUserPlans", &plan_id)?;
    let plan_status = entity_status(&plan);
    if plan_status != "Frozen" {
        return Err(format!(
            "simulated-user plan {plan_id} is '{plan_status}', not Frozen; refusing to queue trials"
        ));
    }
    let plan_fields = state_fields(&plan);
    let direction_id = field_str(episode_fields, &["DirectionId"]);
    // ADR-0023: trials exercise the app through the organism's
    // configured external runtime target — never a hardcoded one.
    let organism_id = field_str(episode_fields, &["OrganismId"]);
    let runtime_target = organism_runtime_target(ctx, base_url, headers, &organism_id)?;
    let users_per_variant = field_u64(&plan_fields, &["UsersPerVariant"]).max(1) as usize;
    let runs_per_persona = field_u64(&plan_fields, &["RunsPerPersona"]).max(1) as usize;
    let personas = parse_json_values(&field_str(&plan_fields, &["PersonasJson"]));
    let goals = parse_json_values(&field_str(&plan_fields, &["GoalsJson"]));
    if personas.len() < users_per_variant {
        return Err(format!(
            "simulated-user plan {plan_id} declares {users_per_variant} users per variant but only {} personas; the frozen census must be consistent",
            personas.len()
        ));
    }
    let mut trial_ids = Vec::new();

    for persona_index in 0..users_per_variant {
        let persona = personas
            .get(persona_index)
            .cloned()
            .unwrap_or_else(|| json!({ "name": format!("codex-simulated-user-{}", persona_index + 1) }));
        let goal = goals
            .get(persona_index)
            .and_then(Value::as_str)
            .map(str::to_string)
            .unwrap_or_else(|| nonempty(stage_name.to_string(), format!("Use variant {variant_id} and report observations.")));
        for run_index in 1..=runs_per_persona {
            let work_item_id = create_entity(ctx, base_url, headers, "WorkItems")?;
            let trial_id = create_entity(ctx, base_url, headers, "Trials")?;
            let simulated_user_id = format!("codex-sim-user-{}-run-{run_index}", persona_index + 1);
            let prompt = simulated_user_prompt(
                stage_fields,
                episode_id,
                &direction_id,
                generation_id,
                variant_id,
                stage_id,
                stage_result_id,
                &trial_id,
                &work_item_id,
                &simulated_user_id,
                persona_index + 1,
                run_index,
                &persona,
                &goal,
                runtime_ref,
                app_ref,
                variant_summary,
                &runtime_target,
            );
            post_paw_orchestration_action(
                ctx,
                base_url,
                headers,
                "WorkItems",
                &work_item_id,
                "QueueWorkItem",
                json!({
                    "Role": "simulated_user",
                    "TargetEntityType": "Trial",
                    "TargetEntityId": trial_id,
                    "PromptRef": format!("literal:{prompt}"),
                    "ContextRef": format!("trial:{trial_id}"),
                    "OutputSchemaRef": "directed-evolution.simulated-user.v1",
                    "RequiredCapabilities": "local_codex,runtime_probe",
                    "Lane": "simulated-user",
                    "ExclusiveKey": "",
                    "CorrelationJson": json!({
                        "episode_id": episode_id,
                        "direction_id": field_str(episode_fields, &["DirectionId"]),
                        "generation_id": generation_id,
                        "variant_id": variant_id,
                        "evaluation_stage_id": stage_id,
                        "stage_result_id": stage_result_id,
                        "trial_id": trial_id,
                        "simulated_user_plan_id": plan_id,
                        "persona_index": persona_index + 1,
                        "run_index": run_index,
                        "runtime_ref": runtime_ref,
                        "app_ref": app_ref,
                        "role": "simulated_user",
                        "runtime_base_url": runtime_target.base_url,
                        "runtime_tenant": runtime_target.tenant,
                        "runtime_auth_env_vars": runtime_target.auth_env_vars,
                        "datadog_service": runtime_target.datadog_service,
                    }).to_string(),
                }),
            )?;
            post_directed_action(
                ctx,
                base_url,
                headers,
                "Trials",
                &trial_id,
                "StartTrial",
                json!({
                    "EpisodeId": episode_id,
                    "GenerationId": generation_id,
                    "VariantId": variant_id,
                    "EvaluationStageId": stage_id,
                    "StageResultId": stage_result_id,
                    "SimulatedUserPlanId": plan_id,
                    "SimulatedUserId": simulated_user_id,
                    "PersonaJson": persona.to_string(),
                    "RunIndex": run_index.to_string(),
                    "Goal": goal,
                    "WorkItemId": work_item_id,
                }),
            )?;
            trial_ids.push(trial_id);
        }
    }
    Ok(trial_ids)
}
